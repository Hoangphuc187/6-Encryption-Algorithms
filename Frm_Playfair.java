  private String thaythe(char a, char b) {
        String vta = timViTri(a);
        String vtb = timViTri(b);
        char x, y;

        if (vta.charAt(0) == vtb.charAt(0)) {
            x = pf[vta.charAt(0) - '0'][((vta.charAt(1) - '0') + 1) % 5];
            y = pf[vtb.charAt(0) - '0'][((vtb.charAt(1) - '0') + 1) % 5];
            return x + "" + y;
        }

        if (vta.charAt(1) == vtb.charAt(1)) { 
            x = pf[((vta.charAt(0) - '0') + 1) % 5][vta.charAt(1) - '0'];
            y = pf[((vtb.charAt(0) - '0') + 1) % 5][vtb.charAt(1) - '0'];
            return x + "" + y;
        }

        x = pf[vta.charAt(0) - '0'][vtb.charAt(1) - '0'];
        y = pf[vtb.charAt(0) - '0'][vta.charAt(1) - '0'];
        return x + "" + y;
    }
 private String timViTri(char a) {
        for (int i = 0; i < 5; i++)
            for (int j = 0; j < 5; j++)
                if (pf[i][j] == a)
                    return i + "" + j;
        return "";}
    
private String thaytheNguoc(char a, char b) {
    String vta = timViTri(a);
    String vtb = timViTri(b);
    char x, y;

    if (vta.charAt(0) == vtb.charAt(0)) {
        x = pf[vta.charAt(0) - '0'][((vta.charAt(1) - '0') + 4) % 5];
        y = pf[vtb.charAt(0) - '0'][((vtb.charAt(1) - '0') + 4) % 5]; 
        return x + "" + y;
    }
    
    if (vta.charAt(1) == vtb.charAt(1)) { 
        x = pf[((vta.charAt(0) - '0') + 4) % 5][vta.charAt(1) - '0']; 
        y = pf[((vtb.charAt(0) - '0') + 4) % 5][vtb.charAt(1) - '0']; 
        return x + "" + y;
    }

    x = pf[vta.charAt(0) - '0'][vtb.charAt(1) - '0'];
    y = pf[vtb.charAt(0) - '0'][vta.charAt(1) - '0'];
    return x + "" + y;
}
private String mahoa(String banro){
        int n = banro.length();
        int i = 0;
        String banma = "";
        char a, b;

        while (i < n) {
            if (i == n - 1) {
                a = banro.charAt(i);
                b = 'X'; 
                i++;
            } else {
                a = banro.charAt(i);
                b = banro.charAt(i + 1);
                if (a == b) {
                    b = 'X';
                    i++;
                } else {
                    i += 2;
                }
            }
            banma += thaythe(a, b);
        }
        return banma;
    }
  private String giaima(String banma) {
    int n = banma.length();
    StringBuilder banro = new StringBuilder();
    char a, b;

    for (int i = 0; i < n; ) {
        if (i == n - 1) { 
            a = banma.charAt(i);
            banro.append(a);
            break;
        } else {
            a = banma.charAt(i);
            b = banma.charAt(i + 1);
            banro.append(thaytheNguoc(a, b)); 
            i += 2; 
        }
    }
    return banro.toString();
}


