 private void taoMaTran(String khoa) {
        StringBuilder sb = new StringBuilder();
        boolean[] daXuatHien = new boolean[26]; 
        khoa = khoa.toUpperCase().replace('J', 'I'); 
        for (char c : khoa.toCharArray()) {
            if (c >= 'A' && c <= 'Z' && !daXuatHien[c - 'A']) {
                sb.append(c);
                daXuatHien[c - 'A'] = true;
            }
        }
        for (char c = 'A'; c <= 'Z'; c++) {
            if (c == 'J') continue; 
            if (!daXuatHien[c - 'A']) {
                sb.append(c);
            }
        }
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                pf[i][j] = sb.charAt(i * 5 + j);
            }
        }
    }
